//  Arithmetic Operators in math

let students = 30;

// students+=1; or students = students+1;
// students-=1; or students = students-1;
// students*=2; or students = students*2;
// students/=2 or students = students/2;
// students**=2; or students = students**2; , exponent 
// students%=2; or students = students%2; , remainder
// students++; , increment
// students--; , decrement
 
console.log(students);

students+=1;

console.log(students);

students-=2;

console.log(students);

students*=3;

console.log(students);

students/=3;

console.log(students);

students**=4;

console.log(students);

students%=2;

console.log(students);

students++;

console.log(students);

students--;

console.log(students);